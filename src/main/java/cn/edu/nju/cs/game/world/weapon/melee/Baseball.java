package cn.edu.nju.cs.game.world.weapon.melee;

import cn.edu.nju.cs.game.world.creature.Creature;
import cn.edu.nju.cs.game.world.item.Tile;

public class Baseball extends AttackDot {

    // 通过此构造函数创造的网球都是样球，用于以后棒球棍的发射棒球的复制
    /**
     * @author: Xu YangXin
     * @param {Creature} master 棒球的主人
     * @return {*}
     * @description: 棒球的构造器
     */    
    public Baseball(Creature master) {
        super(0, 0, BaseballBat.ATTACK_VALUE,
            Tile.BASEBALL, BaseballBat.REMAIN_TIME, master);
    }

    /**
     * @author: Xu YangXin
     * @param {int} x 棒球的x坐标
     * @param {int} y 棒球的y坐标
     * @param {Baseball} baseballSample 棒球的目标模板
     * @return {*}
     * @description: 棒球的复制构造器
     */    
    public Baseball(int x, int y, Baseball baseballSample){
        super(x, y, baseballSample);
    }
}
