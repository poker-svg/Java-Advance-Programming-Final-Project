package cn.edu.nju.cs.game.world.creature;

import java.io.Serializable;

import cn.edu.nju.cs.game.world.item.Tile;

public enum Direction implements Serializable {
    Left("Left", Tile.LEFT),

    Right("Right", Tile.RIGHT),

    Up("Up", Tile.UP),

    Down("Down", Tile.DOWN),

    LeftUp("LeftUp", null),

    LeftDown("LeftDown", null),

    RightUp("RightUp", null),

    RightDown("RightDown", null);

    private String direction;
    /**
     * @author: Xu YangXin
     * @return {String} 方向字符串
     * @description: 读取方向内容
     */    
    public String direction(){
        return direction;
    }

    private Tile image;
    /**
     * @author: Xu YangXin
     * @return {Tile} 对应方向的图像
     * @description: 读取对应方向的图像
     */    
    public Tile image(){
        return this.image;
    }

    /**
     * @author: Xu YangXin
     * @param {String} direction 方向字符串
     * @param {Tile} image 方向的图像
     * @return {*}
     * @description: 构造器
     */
    Direction(String direction, Tile image) {
        this.direction = direction;
        this.image = image;
    }
}
