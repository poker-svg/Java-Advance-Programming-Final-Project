package cn.edu.nju.cs.game.world.creature;

import java.awt.Point;
import java.io.Serializable;

import cn.edu.nju.cs.game.world.item.Tile;
import cn.edu.nju.cs.game.world.item.Line;

class CreatureAI implements Serializable{

    protected Creature creature;

    /**
     * @author: Xu YangXin
     * @param {Creature} creature 生物
     * @return {*}
     * @description: 构造器
     */    
    public CreatureAI(Creature creature) {
        this.creature = creature;
        this.creature.setAI(this);
    }

    public void onEnter(int x, int y, Tile tile) {
    }

    public void onUpdate() {
    }

    public void onNotify(String message) {
    }

    /**
     * @author: Xu YangXin
     * @param {int} x 目标的x坐标
     * @param {int} y 目标的y坐标
     * @return {boolean} 看见与否
     * @description: 生物是否可以看见某个坐标
     */    
    public boolean canSee(int x, int y) {
        if ((creature.x() - x) * (creature.x() - x) + (creature.y() - y) * (creature.y() - y) > creature.visionRadius()
                * creature.visionRadius()) {
            return false;
        }
        Line line = new Line(creature.x(), creature.y(), x, y);
        for (Point p : line) {
            if (creature.tile(p.x, p.y).isGround() || (p.x == x && p.y == y)) {
                continue;
            }
            return false;
        }
        return true;
    }
}
