package cn.edu.nju.cs.connection;

import java.io.*;
import java.util.Optional;

public class ByteArrayUtils {
    /**
     * @author: Xu YangXin
     * @param {T} obj 要被处理的对象
     * @return {Optional<byte[]>} 包含目标字节数组的目标对象
     * @description: 将目标对象的字节流转换成包含字节数组的Optional对象
     */
    public static<T> Optional<byte[]> objectToBytes(T obj){
        byte[] bytes = null;
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        ObjectOutputStream sOut;
        try {
            sOut = new ObjectOutputStream(out);
            sOut.writeObject(obj);
            sOut.flush();
            bytes= out.toByteArray();
        } catch (IOException e) {
            e.printStackTrace();
        }
        return Optional.ofNullable(bytes);
    }

    /**
     * @author: Xu YangXin
     * @param <T> 类型函数
     * @param {byte[]} bytes 要被转换的对象的字节数组
     * @return {Optional<T>}
     * @description: 将目标对象的字节数组转换成对象
     */
    public static<T> Optional<T> bytesToObject(byte[] bytes) {
        T t = null;
        ByteArrayInputStream in = new ByteArrayInputStream(bytes);
        ObjectInputStream sIn;
        try {
            sIn = new ObjectInputStream(in);
            t = (T)sIn.readObject();
        } catch (Exception e) {
            e.printStackTrace();
        }
        return Optional.ofNullable(t);

    }
}